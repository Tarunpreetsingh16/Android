package com.example.sos;

public class Contact {
    String name;
    String number;
    //setter and getters
    public void setName(String name){
        this.name = name;
    }

    public String getName() {
        return name;
    }
    public String getNumber(){
        return number;
    }
    public void setNumber (String number){
        this.number = number;
    }
}
